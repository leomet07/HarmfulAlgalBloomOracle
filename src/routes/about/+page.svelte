<section>
	<hgroup>
		<h1>About</h1>
	</hgroup>
	<p>
		In New York State, reports of HABs have increased annually since over recent decades. However,
		there is scant data to conclude that algal bloom intensity or annual duration has increased
		statewide. Monitoring algal blooms is cost, time, and equipment intensive, and public data
		availability is lacking outside of safe beach monitoring programs.
	</p>

	<p>
		In this study supported by the NASA Climate Change Research Initative and the Center for Remote
		Sensing and Earth System Sciences (ReSESS) at the New York City College of Technology, our team
		successfully developed a <a href="https://github.com/leomet07/ny-satellite-chla-model"
			>machine learning model to predict algal blooms</a
		>. The published research paper can be found at
		<a href="https://doi.org/10.1016/j.ecolind.2025.114316">
			doi.org/10.1016/j.ecolind.2025.114316
		</a>
	</p>

	<p>
		Our team has created this public facing interface for our results where stakeholders and
		interested citizens can view an area of interest over time. Users are able to view time series
		plots of individual lakes, download data, and learn more about the algal bloom issues in New
		York.
	</p>
</section>

